#include <iostream>
using namespace std;

int main() {
    cout << "This is fifteen in decimal: " << dec << 15 << endl;
    cout << "This is fifteen in hexadecimal: " << hex << 15 << endl;
    cout << "This is fifteen in octal: " << oct << 15 << endl;
    cout << "This is a float: " << 3.14159 << endl;
    cout << "The escape character is: " << char(27) << endl;
    return 0;
}
